makeVector <- function(x = numeric()) {
  
  if(!is.numeric(x)){
    stop("Bad input, not a numeric vector");
  }
  
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setmean <- function(mean) m <<- mean
  getmean <- function() m
  list(set = set, get = get,
       setmean = setmean,
       getmean = getmean)
}

